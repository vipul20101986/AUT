package web.pojo;

public class Data {
	
	private String type;
	private String value;
	
	public Data (String type, String value) {
		this.type= type;
		this.value= value;
	}
	
	
	public String getType() {
		return type;
	}
	public void setType(String t1) {
		type = t1;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String v1) {
		value = v1;
	}
	
	
	

}
